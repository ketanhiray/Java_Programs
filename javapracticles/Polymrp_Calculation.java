package javapracticles;

public class Polymrp_Calculation {

	void sum(int a) 
	{
		System.out.print("sum(int a):");
		System.out.println(a + a);
	}
	void sum(double a) 
	{
		System.out.print("sum(double a):");
		System.out.println(a + a);
	}
	void sum(double a ,double b) 
	{
		System.out.print("sum(double a ,double b):");
		System.out.println(a + b);
	}
	void sum(int a,int b) 
	{
		System.out.print("sum(int a,int b):");
		System.out.println(a + b);
	}
	void sum(double a,int b) 
	{
		System.out.print("sum(double a,int b):");
		System.out.println(a + b);
	}
	
		
}
